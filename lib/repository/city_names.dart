  static List<int> posicao = [];

  static listarCidadesUpperCase(String texto) {
    listaEscrevendoUpperCase.clear();
    posicao.clear();

    if (texto.isEmpty == true) {
      posicao.clear();
      listaEscrevendoUpperCase.clear();
    } else {
      for (int i = 0; i < ListaCidadesBrasilUppercase.listaCidadesBrasilUppercase.length; i++) {
        if (ListaCidadesBrasilUppercase.listaCidadesBrasilUppercase[i].startsWith(texto, 0) == true) {
          posicao.add(i);
          listaEscrevendoUpperCase.add(ListaCidadesBrasilUppercase.listaCidadesBrasilUppercase[i]);
        }
      }
    }

    return posicao;
  }

  static String formatarNomeUppercase(String str) {
    var comAcento = 'ÀÁÂÃÄÅàáâãäåÒÓÔÕÕÖØòóôõöøÈÉÊËèéêëðÇçÐÌÍÎÏìíîïÙÚÛÜùúûüÑñŠšŸÿýŽž';
    var semAcento = 'AAAAAAaaaaaaOOOOOOOooooooEEEEeeeeeCcDIIIIiiiiUUUUuuuuNnSsYyyZz';
    for (int i = 0; i < comAcento.length; i++) {
      str = str.replaceAll(comAcento[i], semAcento[i]);
    }

    return str.toUpperCase().trim();
  }
